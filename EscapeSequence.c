#include<stdio.h>
int main()
{
    // This is a single line comment
    /* 
    This is a 
    Multi Line
    Comment 
    */
   printf("\"Never Settle\" - OnePlus\n"); // If you need to include Double Quote
   printf("\'Never Settle\' - OnePlus\n"); // If you need to include Single Quote
   printf("\\Never Settle\\ - OnePlus\n"); // If you need to include Oblique
   printf("A Small Matrix:\n1\t2\t3\n4\t5\t6\n7\t8\t9"); // This is how we can use tab escape sequence
   return 0;
}